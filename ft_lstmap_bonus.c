#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*new;
	t_list	*node;
	void	*r;

	if (!lst || !f || !del)
		return (NULL);
	new = NULL;
	while (lst)
	{
		r = f(lst->content);
		node = ft_lstnew(r);
		if (!node)
		{
			del(r);
			ft_lstclear(&new, del);
			return (NULL);
		}
		ft_lstadd_back(&new, node);
		lst = lst->next;
	}
	return (new);
}
